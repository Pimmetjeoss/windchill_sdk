@mcp.tool()
async def windchill_get_bom(part_id: str, levels: int = 1) -> str:
    """Get the Bill of Materials (BOM) for a part.

    Returns the child components/materials used by this part.

    Args:
        part_id: The Object Reference ID of the parent part.
        levels: Number of BOM levels to retrieve (1 = immediate children only).
            Use 1 for single-level BOM. For multi-level, use
            windchill_get_part_structure instead.

    Returns:
        JSON array of BOM line items (part usage links).
    """
    try:
        client = await get_client()
        if levels > 1:
            result = await client.prod_mgmt.get_part_structure(part_id)
            return json.dumps(result, indent=2, default=str)
        result = await client.prod_mgmt.get_bom(part_id)
        return json.dumps(result.items, indent=2, default=str)
    except Exception as e:
        return json.dumps({"error": str(e)})


