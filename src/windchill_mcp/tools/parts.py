@mcp.tool()
async def windchill_download_part_content(
    part_number: str,
    output_path: str | None = None,
) -> str:
    """Download the primary document/file associated with a part.

    Looks up the part by number, finds its linked document via the
    DescribedBy relationship, and downloads the primary content (e.g., PDF).

    Args:
        part_number: The part number (e.g., "WH806239").
        output_path: Optional file path to save to. If not provided,
            uses the original filename in the current directory.

    Returns:
        JSON with the download result including file path and size.
    """
    try:
        client = await get_client()

        # Find the part by number
        parts = await client.prod_mgmt.list_parts(
            Query().filter(F.eq("Number", part_number)).top(1).latest_version()
        )
        if not parts.items:
            return json.dumps({"error": f"Part {part_number} not found"})

        part = parts.first
        part_id = part["ID"]

        # Get content info
        content_info = await client.prod_mgmt.get_part_content_info(part_id)
        if not content_info:
            return json.dumps({"error": f"No downloadable content found for part {part_number}"})

        # Download
        saved_path = await client.prod_mgmt.download_part_content(part_id, output_path)

        return json.dumps({
            "success": True,
            "part_number": part_number,
            "file_name": content_info.file_name,
            "file_size": content_info.file_size,
            "format": content_info.format,
            "saved_to": saved_path,
        }, indent=2)
    except Exception as e:
        return json.dumps({"error": str(e)})
