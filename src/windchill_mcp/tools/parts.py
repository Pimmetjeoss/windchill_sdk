"""Part management tools for Windchill MCP Server."""

from __future__ import annotations

import json

from windchill.odata.filter import F
from windchill.odata.query import Query

from windchill_mcp.server import get_client, mcp


@mcp.tool()
async def windchill_search_parts(
    filter_expr: str | None = None,
    select: str | None = None,
    top: int = 50,
    latest_version: bool = True,
) -> str:
    """Search for parts in Windchill PLM.

    Args:
        filter_expr: OData filter expression (e.g. "contains(Name,'bracket')",
            "Source eq 'MAKE'", "State eq 'RELEASED'"). Supports standard OData
            operators: eq, ne, gt, lt, contains, startswith, endswith, and/or.
        select: Comma-separated list of properties to return
            (e.g. "ID,Name,Number,State,Source").
        top: Maximum number of results to return (default 50).
        latest_version: If true, return only the latest version of each part.

    Returns:
        JSON array of matching parts with their properties.
    """
    try:
        client = await get_client()
        query = Query().top(top)
        if latest_version:
            query = query.latest_version()
        if filter_expr:
            query = query.filter(F.raw(filter_expr))
        if select:
            query = query.select(*[s.strip() for s in select.split(",")])
        result = await client.prod_mgmt.list_parts(query)
        return json.dumps(result.items, indent=2, default=str)
    except Exception as e:
        return json.dumps({"error": str(e)})


